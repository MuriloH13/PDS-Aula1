package Modelo;

public class Calculos {
	
	public float CalcAdicao(float Prim, float Seg) {
		float Resultado = Prim + Seg;
		return Resultado;
	}

	public float CalcSubtracao(float Prim, float Seg) {
		float Resultado = Prim - Seg;
		return Resultado;
	}
	
	public float CalcMultiplicacao(float Prim, float Seg) {
		float Resultado = Prim * Seg;
		return Resultado;
	}
	
	public float CalcDivisao(float Prim, float Seg) {
		float Resultado = Prim / Seg;
		return Resultado;
	}

}
